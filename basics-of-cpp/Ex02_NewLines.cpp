#include <iostream>
using namespace std;
int main () {
    /* The newline character (\n) is called an escape sequence,
     * and it forces the cursor to change its position to the beginning of the next line on the screen.
     * This results in a new line.
     */
    cout << "Hi, \n";
    cout << "Welcome to cpp." << "\n";
    cout << "Hello there!";

    return 0;
}
